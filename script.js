function showOval() {
    let pupBG  = document.getElementById('pup-map')
    let map = document.getElementById('oval')
    let button = document.getElementById('button-div')

    if (map.style.opacity == 0) {
        map.style.opacity = 100;
        map.style.zIndex = 1;
        pupBG.style["filter"]="blur(3px)";
        button.style.opacity = 0;
    } 
}
function resetOval() {
    let pupBG  = document.getElementById('pup-map');
    let map = document.getElementById('oval');
    let button = document.getElementById('button-div')

    if (map.style.opacity == 100) {
        map.style.opacity = 0;
        map.style.zIndex = 0;
        pupBG.style["filter"]="blur(0px)";
        button.style.opacity = 100;
    }
}

